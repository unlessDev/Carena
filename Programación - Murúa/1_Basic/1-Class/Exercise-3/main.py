#Desarrollar un algoritmo para calcular la superficie de un cuadrado (base por altura):

#Mostrar el mensaje de ingreso y guardar en base.
base = int(input("Ingrese el valor de la base: "))
print()

#Mostrar mensaje de ingreso y guardar en altura.
alt = int(input("Ingrese el valor de la altura "))
print()

#Calcular base * altura y guardar en superficie.
resul = base * alt

print()
print("El resultado final de la superficie del cuadrado es: ", resul)
